// 世界引擎 · 世界状态 schema 与种子数据
// 世界状态存于聊天变量 {type:'chat'} 的「世界引擎」键下，全聊天单一真相，不围绕主角。

const 区域枚举 = z.enum(['中州', '西漠', '东荒', '南岭', '北冥']);
const 势力分类枚举 = z.enum(['正道', '魔道', '古神', '其他']);

// 数值字段统一用 preprocess 兜底空值，再 transform 限幅
const 数值 = (min: number, max: number, 默认: number) =>
  z
    .preprocess(v => (v === null || v === undefined ? 默认 : Number(v)), z.number())
    .transform(v => _.clamp(v, min, max))
    .prefault(默认);

export const 世界状态Schema = z
  .object({
    势力: z
      .record(
        z.string().describe('势力名'),
        z
          .object({
            分类: 势力分类枚举.prefault('其他'),
            影响范围: 数值(0, 100, 30),
            实力: 数值(0, 100, 30),
            态势: z.string().prefault('平稳'),
            内部动向: z.string().prefault(''),
            与主角关系: z.string().prefault('中立'),
            描述: z.string().prefault(''),
          })
          .prefault({}),
      )
      .prefault({}),

    人物动向: z
      .record(
        z.string().describe('人物名'),
        z
          .object({
            称号: z.string().prefault(''),
            势力: z.string().prefault(''),
            当前动向: z.string().prefault(''),
            所在地: z.string().prefault(''),
            状态: z.string().prefault('正常'),
            最近变化: z.string().prefault(''),
          })
          .prefault({}),
      )
      .prefault({}),

    地区事件: z
      .record(
        z.string().describe('地区名'),
        z
          .object({
            区域: 区域枚举.prefault('中州'),
            当前事件: z.string().prefault(''),
            氛围: z.string().prefault(''),
            安全度: 数值(0, 100, 50),
            近期传闻: z.array(z.string()).prefault([]),
          })
          .prefault({}),
      )
      .prefault({}),

    世界事件: z
      .array(
        z
          .object({
            时间: z.string().prefault(''),
            事件: z.string().prefault(''),
            描述: z.string().prefault(''),
            影响: z.string().prefault(''),
          })
          .prefault({}),
      )
      .prefault([]),

    暗流涌动: z
      .record(
        z.string().describe('暗线名'),
        z
          .object({
            涉及势力: z.array(z.string()).prefault([]),
            成熟度: 数值(0, 100, 0),
            描述: z.string().prefault(''),
            是否已爆发: z.boolean().prefault(false),
          })
          .prefault({}),
      )
      .prefault({}),
  })
  .prefault({});

export type 世界状态 = z.output<typeof 世界状态Schema>;

// 独立 API 配置：与主线模型隔离
const 源枚举 = z.enum(['openai', 'claude', 'deepseek']);
export const CustomApiSchema = z
  .object({
    source: 源枚举.prefault('openai'),
    apiurl: z.string().prefault(''),
    key: z.string().prefault(''),
    model: z.string().prefault(''),
  })
  .prefault({});
export type CustomApi = z.output<typeof CustomApiSchema>;

export const 世界引擎Schema = z
  .object({
    世界状态: 世界状态Schema.prefault({}),
    摘要: z
      .object({
        近期摘要: z.string().prefault(''),
        上次摘要楼层: z.coerce.number().prefault(0),
      })
      .prefault({}),
    上次推进楼层: z.coerce.number().prefault(0),
    // 一句话世界氛围：纯函数派生，供世界书常驻条目注入主线 prompt
    世界氛围: z.string().prefault(''),
    配置: z
      .object({
        自动推进: z.boolean().prefault(false),
        自动推进间隔: z.coerce.number().prefault(10),
        摘要间隔: z.coerce.number().prefault(5),
        custom_api: CustomApiSchema.nullable().prefault(null),
      })
      .prefault({}),
  })
  .prefault({});

export type 世界引擎 = z.output<typeof 世界引擎Schema>;

