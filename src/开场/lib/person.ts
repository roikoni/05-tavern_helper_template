/**
 * 判定开场捏角中某位「重要人物」是否被玩家真正设置过。
 *
 * 背景：initvar 预置了五位尘世 NPC 的完整档案（关系默认「中立」、好感 0、攻略 false），
 * 但这些只是占位档案，并非玩家在人脉羁绊步骤里做出的选择；原 prompt 只凭「关系或好感度非空」
 * 过滤，会把占位 NPC 全部误判为"已设置"输出给 AI，导致 AI 把它们一并 insert 进「羁绊」变量，
 * 造成多余的变量更新。
 *
 * "被设置过"的判定口径（任一即为已设置）：
 * 1. 好感度 ≠ 0（玩家拖过好感滑条）
 * 2. 攻略 = true（玩家勾过"已攻略"）
 * 3. 关系被改成与该角色默认关系不同的值（玩家改过关系输入框）
 *
 * 古神 boss：默认好感 0、关系「未知」、攻略 false → 未被显式设置时不会进入提示词，
 * 与「未被设置的角色不进入提示词文本」的要求一致；玩家一旦调整任一字段即视为设置。
 */

/** 该角色在 initvar / catalog 中的默认关系，用于判断关系是否被改动 */
import { 查人物 } from '../catalog/人物';

export function 人物已被设置(名: string, v: any): boolean {
  if (!v) return false;
  if (Number(v.好感度) !== 0) return true;
  if (v.攻略) return true;
  const 默认 = 查人物(名)?.默认关系 ?? '';
  // 关系被改成与默认不同的非空值才算设置；占位的「中立」与 catalog 默认关系都不会误判
  const 关系 = v.关系 ?? '';
  return 关系 !== '' && 关系 !== '中立' && 关系 !== 默认;
}
