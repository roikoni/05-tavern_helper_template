import { Schema } from '../../修仙状态栏/schema';
import { 出身地列表 } from '../catalog/出身地';
import { 功法列表 } from '../catalog/功法';

type StatData = z.infer<typeof Schema>;

const 层级编号: Record<string, number> = {
  黄阶下品: 1,
  黄阶中品: 2,
  黄阶上品: 3,
  玄阶下品: 4,
};

/**
 * 把玩家所有选择拼成结构化的角色档案 + 精简的叙事指令，作为用户消息发给 AI。
 * AI 读取后负责基于此模板生成一段完整的叙事开场剧情。
 *
 * @param stat_data 当前 mvu stat_data
 * @param 已选功法名 玩家在捏角中选定的功法名（来自 draft store，未写入 stat_data）
 */
export function buildOpeningPrompt(stat_data: StatData, 已选功法名: readonly string[] = []): string {
  const 主角 = stat_data.主角;
  const 武器 = 主角.装备?.武器;
  const 法器 = 主角.装备?.法器;

  // 把功法名匹配到 catalog，得到完整字段；找不到则降级为最简单条目
  const 已选功法详 = 已选功法名.map(名 => {
    const c = 功法列表.find(g => g.名称 === 名);
    if (c) {
      return {
        名称: c.名称,
        类型: c.类型,
        层级: 层级编号[c.层级] ?? 1,
        层级名: c.层级,
        效果: c.效果,
      };
    }
    return { 名称: 名, 类型: '被动' as const, 层级: 1, 层级名: '黄阶下品', 效果: '' };
  });

  const 行: string[] = [];

  // ═══════════════════════════════════════════
  // 第一部分：角色档案
  // ═══════════════════════════════════════════
  行.push('=== 角色档案 ===');
  行.push('');
  行.push('【基本信息】');
  行.push(`  姓名: ${主角.名称}`);
  行.push(`  性别: ${主角.性别}`);
  行.push(`  称号: ${主角.称号}`);
  行.push(`  宗门: ${主角.宗门}`);
  行.push(`  出身地: ${主角.出身地}`);
  if (主角.外貌) 行.push(`  人物设定: ${主角.外貌}`);

  行.push('');
  行.push('【道途】');
  行.push(`  本源（道之根本）: ${主角.本源}`);
  行.push(`  修炼流派: ${主角.修炼流派}`);
  行.push(`  灵根: ${主角.灵根}`);
  行.push(`  神契古神: 胧（戏言之蝶）—— 已与角色同行多时，拌嘴中彼此依存`);

  行.push('');
  行.push('【六维属性】');
  行.push(`  力道 ${主角.六维.力道} | 体魄 ${主角.六维.体魄} | 身法 ${主角.六维.身法}`);
  行.push(`  灵力 ${主角.六维.灵力} | 神识 ${主角.六维.神识} | 根骨 ${主角.六维.根骨}`);

  行.push('');
  行.push('【装备与灵石】');
  if (武器 && 武器.名称 !== '空置') {
    行.push(`  武器: ${武器.名称}（${武器.品阶 ?? '凡品'}）${武器.属性 ? ` — ${武器.属性}` : ''}`);
  }
  if (法器 && 法器.名称 !== '空置') {
    行.push(`  法器: ${法器.名称}（${法器.品阶 ?? '凡品'}）${法器.属性 ? ` — ${法器.属性}` : ''}`);
  }
  行.push(`  灵石: ${主角.灵石} 枚`);
  if ((!武器 || 武器.名称 === '空置') && (!法器 || 法器.名称 === '空置')) {
    行.push('  （暂无装备）');
  }

  行.push('');
  行.push('【已学功法】');
  if (已选功法详.length) {
    for (const g of 已选功法详) {
      行.push(`  - ${g.名称}（${g.层级名}·${g.类型}）— ${g.效果}`);
    }
  } else {
    行.push('  （尚未学习功法）');
  }

  // ═══════════════════════════════════════════
  // 第二部分：叙事指令
  // ═══════════════════════════════════════════
  行.push('');
  行.push('===');
  行.push('');
  行.push('【你的任务】');
  行.push(
    `基于以上角色档案，结合世界书既有设定（世界书已在上下文中，自然引用即可，无需复述条目原文），为主角"${主角.名称}"生成一段叙事开场剧情。`,
  );

  const 出身地区域 = 出身地列表.find(p => p.名称 === 主角.出身地)?.区域 ?? '';

  行.push('');
  行.push('【叙事要求】');
  行.push(
    `1. 场景必须发生在出身地"${主角.出身地}"所属区域${出身地区域}，不可偏移至其他区域。先描绘此刻环境（时辰、天气、建筑、人群与生活/修炼气息），再切入主角正在做的具体事——不要写"站在某处发呆"。`,
  );
  行.push(`   宗门弟子从宗门日常切入（晨练/值勤/听训/接任务），散修从坊市/赶路/借宿切入。选一个有张力的时刻起笔。`);
  行.push(
    '2. 世界观自然呈现，不做说明文式罗列：通过环境、路人反应、势力间张力等暗示所在区域的修炼文化，以及主角称号、流派、宗门在当地的影响力。',
  );
  行.push(
    '3. 胧（戏言之蝶）已与主角同行多时，以拟人形态出现，通过一段对话展示"有历史的相伴"——对白带揶揄戏谑，底下是默契关心，可不经意带出对当前地点的看法。',
  );
  行.push(
    '4. 功法与装备不要罗列名称，而是通过此刻情境自然展现（武器特征、法器藏处、功法在行动习惯/呼吸/神态中的流露），六维高低用感官描写暗示而非报数字。',
  );
  行.push('5. 结尾设置一个具体的、从场景中生长出来的悬念钩子，给主角留出明显的行动空间。');
  行.push('6. 变量更新不得遗漏【已学功法】。');
  行.push('7. 文风模仿金庸，全文 800-1200 字。');

  return 行.join('\n');
}
