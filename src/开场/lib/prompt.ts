import { Schema } from '../../修仙状态栏/schema';
import { 出身地列表 } from '../catalog/出身地';
import { 功法列表 } from '../catalog/功法';
import { 神级功法列表 } from '../catalog/神级功法';

type StatData = z.infer<typeof Schema>;

const 层级编号: Record<string, number> = {
  黄阶下品: 1,
  黄阶中品: 2,
  黄阶上品: 3,
  玄阶下品: 4,
  神级: 9,
};

/**
 * 把玩家所有选择拼成结构化的角色档案 + 精简的叙事指令，作为用户消息发给 AI。
 * AI 读取后负责基于此模板生成一段完整的叙事开场剧情。
 *
 * @param stat_data 当前 mvu stat_data
 * @param 已选功法名 玩家在捏角中选定的功法名（来自 draft store，未写入 stat_data）
 * @param _自由 保留参数，不再区分模式
 */
export function buildOpeningPrompt(
  stat_data: StatData,
  已选功法名: readonly string[] = [],
  _自由 = true,
  开局身份 = '',
  本源基调: '正经' | '搞笑' | '涩涩' = '正经',
): string {
  const 主角 = stat_data.主角;
  const 武器 = 主角.装备?.武器;
  const 法器 = 主角.装备?.法器;

  // 把功法名匹配到 catalog，得到完整字段；找不到则降级为最简单条目
  const 已选功法详 = 已选功法名.map(名 => {
    const c = 功法列表.find(g => g.名称 === 名) ?? 神级功法列表.find(g => g.名称 === 名);
    if (c) {
      return {
        名称: c.名称,
        类型: c.类型,
        层级: 层级编号[c.层级] ?? 1,
        层级名: c.层级,
        效果: c.效果,
      };
    }
    return { 名称: 名, 类型: '被动' as const, 层级: 1, 层级名: '黄阶下品', 效果: '' };
  });

  const 行: string[] = [];

  // ═══════════════════════════════════════════
  // 第一部分：角色档案
  // ═══════════════════════════════════════════
  行.push('=== 角色档案 ===');
  行.push('');
  行.push('【基本信息】');
  行.push(`  姓名: ${主角.名称}`);
  行.push(`  性别: ${主角.性别}`);
  if (主角.种族) 行.push(`  种族: ${主角.种族}`);
  行.push(`  称号: ${主角.称号}`);
  行.push(`  宗门: ${主角.宗门}`);
  行.push(`  出身地: ${主角.出身地}`);
  if (开局身份) 行.push(`  开局身份: ${开局身份}`);
  if (主角.外貌) 行.push(`  人物设定: ${主角.外貌}`);

  行.push('');
  行.push('【道途】');
  行.push(`  本源（道之根本）: ${主角.本源}`);
  // 本源基调：影响自悟功法命名/效果与开场剧情笔调
  const 基调描述: Record<typeof 本源基调, string> = {
    正经: '正剧冷峻，克制庄重，功法命名古韵、效果写实',
    搞笑: '戏谑荒诞，插科打诨，功法命名可带谐趣、效果自带反差',
    涩涩: '暧昧香艳，情欲暗涌，功法命名可带媚意、效果偏双修采补',
  };
  行.push(`  叙事基调: ${本源基调}（${基调描述[本源基调]}）`);
  行.push(`  修炼流派: ${主角.修炼流派}`);
  行.push(`  灵根: ${主角.灵根}`);
  行.push(`  修为境界: ${主角.境界}（修为 ${主角.修为}/${主角.修为上限}）`);
  行.push(`  善恶值: ${主角.善恶值} · san值: ${主角.san值}`);

  // 神契古神：从古神列表读取已收服者；并展示神契装备
  {
    const 古神列表 = _.get(stat_data, '古神列表') as Record<string, any> | undefined;
    const 已收服 = _.entries(古神列表 ?? {}).filter(([, v]) => v && v.收服);
    const 神契装备 = 主角.神契装备;
    if (已收服.length) {
      行.push('  神契古神（已收服·与主角同行）:');
      for (const [名, v] of 已收服) {
        const 位阶 = v.位阶 ?? '';
        const 领域 = v.领域 ?? '';
        行.push(
          `    - ${名}${位阶 ? `（${位阶}·${领域}）` : ''}${神契装备 && 神契装备.includes(名) ? '【当前装备神契】' : ''}`,
        );
      }
    } else if (神契装备) {
      行.push(`  神契古神: ${神契装备}`);
    } else {
      行.push('  神契古神: 无');
    }
  }

  行.push('');
  行.push('【六维属性】');
  行.push(`  力道 ${主角.六维.力道} | 体魄 ${主角.六维.体魄} | 身法 ${主角.六维.身法}`);
  行.push(`  灵力 ${主角.六维.灵力} | 神识 ${主角.六维.神识} | 根骨 ${主角.六维.根骨}`);

  行.push('');
  行.push('【装备与灵石】');
  if (武器 && 武器.名称 !== '空置') {
    行.push(`  武器: ${武器.名称}（${武器.品阶 ?? '凡品'}）${武器.属性 ? ` — ${武器.属性}` : ''}`);
  }
  if (法器 && 法器.名称 !== '空置') {
    行.push(`  法器: ${法器.名称}（${法器.品阶 ?? '凡品'}）${法器.属性 ? ` — ${法器.属性}` : ''}`);
  }
  行.push(`  灵石: ${主角.灵石} 枚`);
  if ((!武器 || 武器.名称 === '空置') && (!法器 || 法器.名称 === '空置')) {
    行.push('  （暂无装备）');
  }

  行.push('');
  行.push('【已学功法】');
  if (已选功法详.length) {
    for (const g of 已选功法详) {
      行.push(`  - ${g.名称}（${g.层级名}·${g.类型}）— ${g.效果}`);
    }
  } else {
    行.push('  （尚未学习功法）');
  }

  // 人脉羁绊 + 世界时间
  {
    const 重要人物 = _.get(stat_data, '重要人物') as Record<string, any> | undefined;
    const 人脉行 = _.entries(重要人物 ?? {})
      .filter(([名, v]) => v && (v.好感度 !== 0 || v.攻略 || (v.关系 && v.关系 !== '陌路人' && v.关系 !== '中立')))
      .map(([名, v]) => {
        const 好感 = v.好感度 ?? 0;
        const 关系 = v.关系 ?? '陌路人';
        const 攻略 = v.攻略 ? '·已攻略' : '';
        return `  - ${名}：${关系}（好感 ${好感}${攻略}）`;
      });
    if (人脉行.length) {
      行.push('');
      行.push('【人脉羁绊】');
      行.push('  以下为玩家预设的、主角开局已建立的重要人际关系（非陌生人）:');
      人脉行.forEach(s => 行.push(s));
    }

    const 世界 = _.get(stat_data, '世界') as { 当前年号?: string; 当前时间?: string } | undefined;
    if (世界 && (世界.当前年号 || 世界.当前时间)) {
      行.push('');
      行.push('【世界时间】');
      行.push(`  年号: ${世界.当前年号 ?? '太初'} · 时间: ${世界.当前时间 ?? '混沌历元年'}`);
    }
  }

  // ═══════════════════════════════════════════
  // 第二部分：叙事指令
  // ═══════════════════════════════════════════
  行.push('');
  行.push('===');
  行.push('');
  行.push('【你的任务】');
  行.push(`基于以上角色档案，为主角"${主角.名称}"生成一段叙事开场剧情。`);

  const 出身地区域 = 出身地列表.find(p => p.名称 === 主角.出身地)?.区域 ?? '';

  行.push('');
  行.push('【叙事要求】');
  行.push(
    `1. 场景必须发生在出身地"${主角.出身地}"所属区域${出身地区域}，不可偏移至其他区域。先描绘此刻环境（时辰、天气、建筑、人群与生活/修炼气息），再切入主角正在做的具体事。`,
  );
  行.push(`   宗门弟子从宗门日常切入（晨练/值勤/听训/接任务），散修从坊市/赶路/借宿切入。选一个有张力的时刻起笔。`);
  行.push(
    '2. 世界观自然呈现，不做说明文式罗列：通过环境、路人反应、势力间张力等暗示所在区域的修炼文化，以及主角称号、流派、宗门在当地的影响力。',
  );
  行.push(
    '4. 功法与装备不要罗列名称，而是通过此刻情境自然展现（武器特征、法器藏处、功法在行动习惯/呼吸/神态中的流露），六维高低用感官描写暗示而非报数字。',
  );
  行.push('5. 结尾设置一个具体的、从场景中生长出来的悬念钩子，给主角留出明显的行动空间。');
  行.push('6. 变量更新不得遗漏【已学功法】。');

  // 第 3 条：神契古神据档案动态生成
  {
    const 古神列表 = _.get(stat_data, '古神列表') as Record<string, any> | undefined;
    const 已收服古神 = _.entries(古神列表 ?? {})
      .filter(([, v]) => v && v.收服)
      .map(([名]) => 名);
    if (已收服古神.length) {
      const 主名 = 已收服古神.includes('胧') ? '胧' : 已收服古神[0];
      const 其余 = 已收服古神.filter(n => n !== 主名);
      let line3 = `3. 神契古神（${已收服古神.join('、')}）已与主角同行多时，`;
      if (主名 === '胧') {
        line3 += '胧（戏言之蝶）以拟人形态出现，通过一段对话展示"有历史的相伴"——对白带揶揄戏谑，底下是默契关心';
      } else {
        line3 += `其中${主名}须以可感形态（拟人/异象/低语等契合其神格的方式）现身，展现"既有羁绊"的相处质感`;
      }
      if (其余.length) line3 += `；${其余.join('、')}亦可在开场中以神契共鸣/感知/暗中呼应等方式自然带出`;
      line3 += '。';
      行.push(line3);
    } else {
      // 无神契古神时的通用条目
      行.push('3. 若主角已有神契古神同行，以拟人/异象形态自然出现，展示"有历史的相伴"。');
    }
  }

  // 文风基调后缀：据本源基调在文风要求中叠加笔调指引
  const 文风基调 = (() => {
    switch (本源基调) {
      case '搞笑':
        return '；但整体笔调插科打诨、带有荒诞谐趣，可适当加入反差与吐槽，不可流于低俗';
      case '涩涩':
        return '；可融入暧昧香艳的情欲暗涌（点到为止，留余韵），场景与互动多一层媚色渲染';
      default:
        return '';
    }
  })();

  行.push('7. 主角种族、修为境界、善恶/san 已由玩家设定，开场需与之自洽');
  行.push(
    '   高境界者应展现相应修为带来的气度与权柄感（大乘期近乎超脱凡俗，凡夫俗子难窥其境），六维强弱须与境界匹配（不得出现大乘期却手无缚鸡之力的描写）；',
  );
  行.push('   非人种族需体现其种族特征；');
  行.push('   若含【世界时间】，以该纪元为时间锚点。');
  行.push(`8. 文风模仿金庸，全文 800-1200 字${文风基调}。`);

  return 行.join('\n');
}
